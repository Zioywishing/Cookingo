/**
 * 菜谱支持的难度等级。
 */
export enum RecipeDifficulty {
  /** 适合新手，或整体操作强度较低。 */
  Easy = "easy",
  /** 需要一定基础下厨经验。 */
  Medium = "medium",
  /** 对火候、时机或技巧要求更高。 */
  Hard = "hard",
}

/**
 * 菜谱元信息中的食材摘要，便于开做前快速总览。
 */
export interface IRecipeIngredientMeta extends IRecipeIngredient {
  /** 可选的分组标签，例如“酱汁”“装饰”。 */
  group?: string;
  /** 标记该食材是否为可选。 */
  optional?: boolean;
}

export interface IRecipeIngredient {
  /** 食材展示名称。 */
  name: string;
  /** 食材用量。 */
  count: {
    /** 数值 */
    value: number;
    /** 单位 */
    unit: string;
  };
}

/**
 * 菜谱中会使用到的厨具。
 */
export interface IRecipeEquipment {
  /** 厨具展示名称。 */
  name: string;
  /** 可选的分组标签，例如“加热”“处理”“盛装”。 */
  group?: string;
  /** 标记该厨具是否为可选。 */
  optional?: boolean;
}

/**
 * 菜谱的顶层摘要信息，用于列表展示、筛选，以及开做前的信息确认。
 */
export interface IRecipeMeta {
  /** 菜谱的稳定唯一标识。 */
  id: string;
  /** 菜谱名称。 */
  name: string;
  /** 对菜品特点的简要描述。 */
  description: string;
  /** 开始烹饪前需要确认的完整食材总览。 */
  ingredients: IRecipeIngredientMeta[];
  /** 开始烹饪前需要准备的厨具总览。 */
  equipment?: IRecipeEquipment[];
  /** 用于搜索和分类的标签。 */
  tags: string[];
  /** 列表页或详情页使用的封面图地址。 */
  coverImageUrl: string;
  /** 份数。默认为 1 */
  servings?: number;
  /** 菜谱整体难度估计。 */
  difficulty?: RecipeDifficulty;
  /** 菜谱作者或维护者。 */
  author?: string;
  /** 原始来源链接或引用说明。 */
  source?: string;

  /** 创建时间，建议使用 ISO 8601 格式。 */
  createdAt: string;
  /** 最后更新时间，建议使用 ISO 8601 格式。 */
  updatedAt: string;
}

export type IRecipeProcessNodeId = number

/**
 * 烹饪流程中的单个可执行节点。
 * 节点是构成流程链的最小执行单元；链通过多个节点顺序连接，
 * 阶段则通过多条链的尾节点与下一阶段的起始链进行对齐。
 *
 * 链实现思路：
 * 1. 一个节点只应表达一个明确、可执行、可判断完成的动作。
 *    例如“切青菜”“热锅”“下鸡丁翻炒 2 分钟”都适合作为单独节点；
 *    不应把“切菜、调汁、热锅”混写在同一个节点中。
 * 2. 同一条链中的节点，应围绕同一条任务线连续推进。
 *    例如“处理鸡肉 -> 腌制 -> 下锅滑炒”可以是一条链；
 *    “切配菜”通常不应插入这条链中间，而应属于另一条链。
 * 3. 若上一步完成后可以自然进入下一步，且没有额外依赖，
 *    则应继续留在当前链中，并通过 `next` 串联后续节点。
 * 4. 若后续动作需要等待多条链的结果同时就绪，
 *    则当前链应在各自尾节点处结束，并开启一条新的链；
 *    新链的头节点应通过 `prev` 依赖前面多条链的尾节点，用于表达汇合。
 * 5. 若某一步是“操作完成后进入等待”，则应设计为带 `async` 的独立节点。
 *    例如“腌制 20 分钟”“炖煮 40 分钟”都应是单独节点，
 *    而不应与等待后的后续动作混写在同一个步骤中。
 * 6. 若某节点会产出后续链要继续使用的中间结果，
 *    应通过 `intermediateIngredients` 明确声明，例如“腌好的鸡丁”“调好的炒汁”。
 * 7. 每条链的尾节点都应具有明确完成态，
 *    以便后续新链能够通过 `prev` 正确依赖它，并完成阶段对齐或跨链汇合。
 *
 * 编排示范：
 * 例如做“清炒鸡丁配时蔬”时，不应写成一条链：
 * - 处理鸡肉 -> 切青菜 -> 调炒汁 -> 下锅合炒
 *
 * 更合理的实现方式是拆成多条链：
 * - 链 A：处理鸡肉 -> 腌制 20 分钟
 * - 链 B：洗青菜 -> 切青菜
 * - 链 C：调炒汁
 *
 * 随后再开启一条新链：
 * - 链 D：热锅 -> 下鸡丁 -> 下青菜 -> 淋炒汁收汁
 *
 * 其中链 D 的头节点“热锅”或“下鸡丁”，应通过 `prev`
 * 依赖链 A、链 B、链 C 的尾节点，表示只有前面的准备都完成后，
 * 才能进入这一条新的烹饪链。
 */
export interface IRecipeProcessNode {
  id: IRecipeProcessNodeId
  /**
   * 步骤标题，可选。
   * 适合在 UI 中作为简短标签展示，用来帮助用户快速识别当前步骤。
   */
  title?: string;
  /**
   * 当前节点要执行的核心动作说明。
   * 应让用户只看这一段也知道这一刻具体要做什么。
   */
  description: string;
  /**
   * 当前节点开始前必须完成的前置节点列表。
   * 用于表达“多个步骤都完成后，才能进入当前节点”的汇合关系。
   * 通常与多个async节点搭配使用。
   * prev中所有节点必须存在于当前的IRecipeProcessStack
   *
   * 使用场景：
   * 例如“切好配菜”和“调好酱汁”都完成后，才允许进入“下锅翻炒”。
   */
  prev?: IRecipeProcessNodeId[];
  /**
   * 当前节点完成后默认进入的下一节点。
   * 若不存在，表示这条流程链在这里结束。
   *
   * 使用场景：
   * 适合表达稳定的连贯的顺序流程，例如“热锅”完成后进入“下油”，再进入“爆香”。
   */
  next?: IRecipeProcessNode;
  /**
   * 异步等待配置。
   * 适用于“腌 20 分钟”“炖煮 40 分钟”这类发起后进入等待的步骤。
   * 该节点执行完成后，不会立刻推进到 `next`；节点会先从 `IRecipeProcessStack` 中移出，
   * 等待结束后再把 `next` 重新压回栈顶继续执行。
   *
   * 使用场景：
   * 适合表示等待期间用户可以去做别的步骤的流程，例如“鸡翅腌制 30 分钟”时，
   * 当前节点先挂起，30 分钟后再继续next事件“开始烘烤”。
   */
  async?: {
    /**
     * 等待时长，单位为分钟。
     * 倒计时结束后才允许进入 `next`。
     */
    waitMinutes: number;
  };
  /**
   * 当前节点本身的操作耗时估计，单位为分钟。
   * 这里只统计“动手操作”的时间，不包含 `async.waitMinutes` 的等待时长。
   */
  durationMinutes?: number;
  /**
   * 当前节点会消耗、处理或明确依赖的食材清单。
   * 用于步骤内展示，也可用于状态追踪或缺料提醒。
   */
  ingredients?: IRecipeIngredient[];
  /**
   * 当前节点会使用到的厨具清单。
   * 用于在开始该步骤前提示用户准备对应器具。
   */
  equipment?: IRecipeEquipment[];
  /**
   * 当前节点产出的中间结果。
   * 例如“腌好的鸡翅”“调好的酱汁”，供后续节点引用。
   */
  intermediateIngredients?: IRecipeIngredient[];
  /**
   * 补充提示、观察要点或易错提醒。
   * 例如火候判断、状态变化。
   */
  tips?: string[];
}

/**
 * 表示一个阶段内的流程链入口集合。
 * 数组中的每个元素，都是一条流程链的头节点；链内部通过 `next` 串联，
 * 链之间通过 `prev` 表达汇合关系或阶段切换依赖。
 *
 * 编排示范：
 * 以“清炒鸡丁配时蔬”为例，可以先拆成 3 个阶段：
 * 1. 准备阶段
 * 2. 烹饪阶段
 * 3. 收尾阶段
 *
 * 其中“准备阶段”的 `IRecipeProcessStack` 可以包含 3 条链：
 * - 链 A：洗青菜 -> 切青菜
 * - 链 B：切鸡胸肉 -> 加盐和料酒抓匀 -> 腌制 20 分钟
 * - 链 C：调炒汁 -> 静置备用
 *
 * “烹饪阶段”的 `IRecipeProcessStack` 也可以包含多条起始链，例如：
 * - 链 D：热锅 -> 下油 -> 下鸡丁滑炒
 * - 链 E：烧水 -> 焯青菜
 *
 * 那么链 D 和链 E 的头节点，都应通过 `prev`
 * 同时依赖准备阶段所有链的尾节点：
 * - 链 A 的尾节点：切青菜
 * - 链 B 的尾节点：腌制 20 分钟
 * - 链 C 的尾节点：静置备用
 *
 * 这样表达的语义是：
 * 只有“青菜准备好”“鸡丁腌好”“炒汁备好”全部完成后，
 * 才能整体进入下一个阶段，而不是其中一条链先完成就提前开炒。
 *
 * 若“腌制 20 分钟”使用了 `async`，也仍然属于准备阶段；
 * 在该节点等待完成之前，准备阶段不应视为结束。
 *
 * 最后进入“收尾阶段”时，例如：
 * - 链 F：合炒鸡丁与青菜 -> 淋入炒汁收汁 -> 装盘
 *
 * 则链 F 的头节点应继续通过 `prev`
 * 依赖烹饪阶段所有链的尾节点，例如“下鸡丁滑炒”与“焯青菜”。
 * 这样可以保证每个新阶段，都是在上一个阶段整体完成后再开始。
 *
 * 设计思路：
 * 1. 先按阶段拆分整道菜。
 * 2. 每个阶段内，再按工作线拆成多条链，每条链只描述一条连续任务线。
 * 3. 每个阶段中，用时相对较长的链应尽量排在前面，
 *    例如腌制、静置、炖煮这类链通常应早于切配或收尾链开始设计。
 * 4. 链内用 `next` 表达顺序，避免把多个独立动作混在同一个节点中。
 * 5. 跨阶段时，让新阶段所有起始链都通过 `prev`
 *    对齐并等待上一阶段所有尾节点。
 */
export type IRecipeProcessStack = IRecipeProcessNode[];

/**
 * 完整菜谱结构，包含摘要信息与流程步骤。
 */
export interface IRecipeSchema {
  /** 菜谱摘要与食材总览。 */
  meta: IRecipeMeta;
  /**
   * 按顺序排列的准备阶段流程，存储该阶段所有流程链表的头节点。
   * 这里只应放需要提前开始、且耗时明显较长的准备工作，
   * 例如提前腌制、浸泡、冷藏静置、长时间回温等。
   * 若只是普通的切配、调汁、洗菜等可紧接烹饪执行的短准备步骤，
   * 则不应放在这里，而应直接编排到 `process` 中。
   */
  prepare?: IRecipeProcessStack;
  /**
   * 按顺序排列的正式流程，存储该阶段所有流程链表的头节点。
   * 除 `prepare` 中那些必须提前进行的长时间准备工作外，
   * 其余步骤都应放在这里，包括普通备料、切配、调汁、加热、烹饪与收尾装盘。
   */
  process: IRecipeProcessStack;
}
